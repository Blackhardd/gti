function initMaps() {
    const icon = {
        anchor: new google.maps.Point(22, 31),
        url: "data:image/svg+xml,%3Csvg width='22' height='31' fill='none' xmlns='http://www.w3.org/2000/svg'%3E%3Cpath d='M10.7 1C5 1 .3 5.7.3 11.4.3 18.6 10.7 31 10.7 31s10.5-12.7 10.5-19.6C21.2 5.7 16.5 1 10.7 1ZM14 14.5a4.4 4.4 0 0 1-6.3 0 4.5 4.5 0 1 1 6.3 0Z' fill='%23E60E0E'/%3E%3C/svg%3E"
    }

    const map_wrappers = document.querySelectorAll('.map')

    map_wrappers.forEach(function(map_wrapper){
        const map = new google.maps.Map(map_wrapper, {
            styles: maps_theme,
            center: {
                lat: parseFloat(gti_maps_data.center.lat),
                lng: parseFloat(gti_maps_data.center.lng)
            },
            zoom: 12,
            zoomControl: false,
            streetViewControl: false,
            mapTypeControl: false,
            rotateControl: false,
            fullscreenControl: false
        })

        new google.maps.Marker({ map, icon, animation: google.maps.Animation.BOUNCE, position: {
            lat: parseFloat(gti_maps_data.center.lat),
            lng: parseFloat(gti_maps_data.center.lng)
        }})
    })
}